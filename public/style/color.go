// Change level of steps for
func (c Color) ChangeStep(step int) Color {
	steps := []string{"50", "100", "200", "300", "400", "500", "600", "700", "800", "900", "950"}
	strColor := string(c)
	s := strings.Split(strColor, "-")
	for index, v := range steps {
		if s[1] == v {
			newIndex := index + step
			if newIndex < 0 || newIndex >= len(steps) {
				return c
			}
			return Color(fmt.Sprintf("%s-%s", s[0], steps[newIndex]))
		}
	}
	return c
}
